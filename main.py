# REGISTER ROUTE SHOULD HASH AND SALT PASSWORD AND SAVE USER DETAILS TO THE DB
@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    password = form.password.data
    if form.validate_on_submit():
        result = db.session.execute(db.select(User).where(User.email == form.email.data))
        user = result.scalar()
        if not user:
            new_user = User(
                name=form.name.data,
                email=form.email.data,
                password=generate_password_hash(password, method='pbkdf2:sha256', salt_length=8)
            )
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect(url_for("get_all_posts"))
        else:
            flash("You've already signed up with that email - please log in instead!")
            return redirect(url_for('login'))
    return render_template("register.html", form=form, current_user=current_user)


# LOGIN ROUTE SHOULD CHECK FOR EMAIL IN DB AND CONFIRM PASSWORD IS ALSO CORRECT
@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        password = form.password.data
        result = db.session.execute(db.select(User).where(User.email == form.email.data))
        user = result.scalar()
        if not user:
            flash("There is no account registered with that email, please try again.")
            return redirect(url_for('login'))
        elif not check_password_hash(user.password, password):
            flash('The password provided is incorrect, please try again.')
            return redirect(url_for('login'))
        else:
            login_user(user)
            return redirect(url_for('get_all_posts'))
    return render_template("login.html", form=form, current_user=current_user)


